<div class="tituloitem">Preditor de Sucesso Cinematográfico [Palestra]</div>
<div class="nomeitem">Juliana Vianna Valério</div>
<div class="conteudo">Na metade do século XX surgiu o termo “indústria cultural”, nomeando o segmento da indústria de entretenimento e cultura, que visa idealizar produtos para o consumo das massas, assim como também pode determinar esse consumo trabalhando sobre o estado de consciência e inconsciência das pessoas.
Um dos desafios dessa área é justamente encontrar uma forma eficaz de divulgar seus produtos/serviços, além de alcançar um bom retorno com a venda dos mesmos.
Com a recente popularização das redes sociais e através da utilização de seus próprios membros, surge a oportunidade de facilitar a difusão de informação em relação a produtos existentes ou a serem lançados pela indústria cultural.
O caráter subjetivo presente na avaliação desses produtos torna qualquer previsão de sucesso incerta, mesmo quando feita por especialistas. Além disso, poucos trabalhos de base matemática buscaram encontrar um equilíbrio entre a influência e outros fatores que levam um indivíduo a adquirir um serviço ou produto da indústria cultural.

A proposta dessa palestra é apresentar a investigação, através de um modelo matemático, como as relações humanas contribuem para o sucesso de produtos da indústria cultural, em especial, as produções cinematográficas. O trabalho inspirou-se em um dos modelos epidemiológicos existentes na literatura: o modelo Suscetíveis-Infectados-Recuperados (SIR), no uso de influência entre indivíduos de uma população e no fenômeno de majoritariedade entre grupos de pessoas (Comportamento de Rebanhos).</div>

Nascida em 30 de dezembro de 1943, a Universidade Federal Rural do Rio de Janeiro, que foi eleita como a oitava universidade mais bonita do mundo pela revista Tam Nas Nuvens, oferece o curso de Sistemas de Informação.<br /><br />
O campus seropedica da UFRRJ localiza-se no KM 47 de Seropédica, cidade na baixada do Estado do Rio de Janeiro.<br /><br />

A partir de 2010, com o início do funcionamento do Curso de Sistemas de Informação da UFRRJ e a chegada dos professores contratados pelo REUNI, o Departamento de Matemática cresceu em número de professores e aumentou as disciplinas oferecidas, chegando a um contingente de mais de 50 professores.<br /><br />

Dentro da área de Computação, essa data marcou também o início da oferta de atividades sustentadas no tripé que norteia a academia (ensino/pesquisa/extensão) tendo a área de Computação como foco. Expressivas atividades de pesquisa, financiadas em sua maioria pela FAPERJ tiveram início com a chegada de um novo grupo de professores desta área, em um total de 10 novas contratações.<br /><br />

Tradicionalmente uma área de suporte a outros cursos de graduação, implantar um curso de graduação em Computação foi sempre uma aspiração dos docentes desse grupo de professores, dentro do Departamento de Matemática da UFRRJ.<br /><br />

Em 2009, ainda contando com apenas três professores da área de computação (dois estavam afastados, em doutoramento), foi assumido o desafio proposto pela Pró-Reitoria de Graduação (Prograd) de elaborar o projeto pedagógico que daria origem ao curso de Bacharelado em Sistemas de Informação. Esse projeto foi finalmente realizado no ano de 2010 com a chegada da primeira turma de alunos. No momento, o curso aguarda a visita dos representantes do MEC para efetivar sua regulamentação, o que deve ocorrer ainda em 2013.<br /><br />

Ao mesmo tempo em que ocorreu a criação da graduação em Sistemas de Informação, o grupo de docentes da área de computação do Departamento de Matemática, hoje composto por 13 professores efetivos, realizou diversas atividades, acadêmicas e administrativas, que serão expostas no decorrer deste documento. Mais quatro professores da área de computação serão contratos via concurso público ainda em 2013, aumentando ainda mais a oferta de disciplinas optativas e projetos de pesquisa em computação.

